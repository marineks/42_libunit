#include <string.h>
#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*a;

	if (n != 0)
	{
		a = (char *)s;
		while (n)
		{
			*a = '\0';
			a++;
			n--;
		}
	}
}
