#include "libft.h"
#include <stdlib.h>

char	*ft_strmap(char const *s, char (*f) (char))
{
	char	*str;
	size_t	cnt;

	cnt = 0;
	if (!s)
		return (NULL);
	str = (char *)malloc(sizeof(char) * (ft_strlen(s) + 1));
	if (!str)
		return (NULL);
	while (s[cnt])
	{
		str[cnt] = f(s[cnt]);
		cnt++;
	}
	str[cnt] = '\0';
	return (str);
}
